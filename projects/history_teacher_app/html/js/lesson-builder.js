/**
 * Сборка текстов уроков (та же логика, что в app.py / lesson_builder).
 */

function makeBulletList(items) {
  return items.map((item) => `- ${item}`).join("\n");
}

function buildLessonFromBank(topic, lessonType, duration, grade, data, extraMaterial) {
  const keyDatesText = makeBulletList(data.key_dates);
  const keyTermsText = data.key_terms.join(", ");
  const sourcesText = makeBulletList(data.sources);
  const extraBlock = extraMaterial.trim()
    ? `\n\nДОПОЛНИТЕЛЬНЫЙ МАТЕРИАЛ УЧИТЕЛЯ:\n\n${extraMaterial}\n`
    : "";

  return `
ТЕМА УРОКА:
${topic}

КЛАСС:
${grade}

ПРЕДМЕТ:
История Алтая и Алтайского края

ТИП УРОКА:
${lessonType}

ПРОДОЛЖИТЕЛЬНОСТЬ:
${duration}

ПЕРИОД:
${data.period}


1. КРАТКАЯ ХАРАКТЕРИСТИКА ТЕМЫ

${data.short_description}


2. ЦЕЛЬ УРОКА

Сформировать у учащихся первичное и осмысленное представление по теме «${topic}», показать её значение для понимания истории Алтая и Алтайского края, а также связать региональную историю с историей России.


3. ЗАДАЧИ УРОКА

Образовательные:
- познакомить учащихся с основными фактами по теме;
- объяснить ключевые понятия простым и понятным языком;
- показать связь истории Алтая с историей России;
- сформировать понимание отличия между Алтаем как историко-географической территорией и Алтайским краем как административной единицей.

Развивающие:
- развивать умение слушать исторический рассказ;
- учить выделять главное в объяснении учителя;
- развивать навык отвечать на вопросы полными предложениями;
- формировать умение делать выводы.

Воспитательные:
- воспитывать интерес к истории родного края;
- формировать уважительное отношение к историческому прошлому;
- показать ценность знания истории своей малой родины.


4. КЛЮЧЕВЫЕ ДАТЫ И ОРИЕНТИРЫ

${keyDatesText}


5. КЛЮЧЕВЫЕ ПОНЯТИЯ

${keyTermsText}


6. ОБОРУДОВАНИЕ И МАТЕРИАЛЫ

- карта Алтая и Алтайского края;
- доска или экран;
- тетради учащихся;
- иллюстрации природы, городов, исторических мест Алтая;
- подготовленный текст урока для учителя.


7. ПЛАН УРОКА

1. Организационный момент — 2 минуты.
2. Вступительная беседа — 5 минут.
3. Объяснение нового материала — 20 минут.
4. Вопросы по ходу объяснения — 5 минут.
5. Закрепление материала — 8 минут.
6. Подведение итогов — 4 минуты.
7. Домашнее задание — 1 минута.


8. ХОД УРОКА

1. Организационный момент.

Учитель приветствует учащихся, проверяет готовность класса к уроку и сообщает тему занятия.

Сегодня мы продолжаем знакомство с историей родного края. История региона помогает нам понять, как жили люди на этой земле, какие события происходили здесь и почему прошлое Алтая важно для нас сегодня.


2. Вступительная беседа.

Учитель задаёт вопросы классу:

- Что вы представляете, когда слышите слово «Алтай»?
- Чем может отличаться Алтай от Алтайского края?
- Почему важно знать историю места, где мы живём?
- Можно ли изучать историю России без истории отдельных регионов?

После ответов учащихся учитель подводит к теме урока.


3. Объяснение нового материала.

${data.teacher_text}
${extraBlock}


4. Вопросы для обсуждения с классом.

1. Какова главная мысль сегодняшнего урока?
2. Какие новые слова или понятия встретились на уроке?
3. Почему важно различать Алтай и Алтайский край?
4. Как история родного края связана с историей России?
5. Что из материала показалось вам самым важным?


5. Задания для закрепления.

Задание 1. Найдите главное.

Запишите в тетрадь 3 главных факта сегодняшнего урока.

Задание 2. Объясните своими словами.

Объясните одно из понятий:
- Алтай;
- Алтайский край;
- малая родина;
- региональная история.

Задание 3. Продолжите фразу.

«Историю Алтая важно изучать, потому что...»

Задание 4. Составьте вопрос.

Придумайте один вопрос по теме урока для одноклассников.


6. Мини-проверка понимания.

1. Как называется тема урока?
2. Что нового вы узнали?
3. Какие даты или факты были важными?
4. Почему история Алтая не начинается только с появления Алтайского края?
5. Какой вывод можно сделать по уроку?


7. Примерный вывод урока.

Сегодня мы узнали, что история Алтая шире истории современного Алтайского края. Алтай — это большая историко-географическая территория с древним прошлым, а Алтайский край как административная единица появился позднее. Изучая историю родного края, мы лучше понимаем прошлое своей малой родины и её связь с историей России.


8. Домашнее задание.

Вариант 1.
Написать 7–10 предложений на тему: «Почему важно изучать историю Алтая?»

Вариант 2.
Составить 5 вопросов по теме урока.

Вариант 3.
Подготовить небольшое сообщение об одном историческом или природном объекте Алтая.

Вариант 4.
Нарисовать схему: «Алтай — Алтайский край — моя малая родина».


9. ПЛАН ЗАПИСИ НА ДОСКЕ

Тема: ${topic}

Главные понятия:
${keyTermsText}

Главные вопросы урока:
1. Что такое Алтай?
2. Чем Алтай отличается от Алтайского края?
3. Почему важно изучать историю родного края?

Главный вывод:
История Алтая намного шире истории современного Алтайского края. Алтайский край — часть большой истории Алтая и России.


10. ИСТОЧНИКИ ДЛЯ УЧИТЕЛЯ

${sourcesText}
`.trim();
}

function buildCustomLesson(topic, lessonType, duration, grade, sourceMaterial, links) {
  const linksBlock = links.trim() || "Ссылки не указаны.";

  return `
ТЕМА УРОКА:
${topic}

КЛАСС:
${grade}

ПРЕДМЕТ:
История Алтая и Алтайского края

ТИП УРОКА:
${lessonType}

ПРОДОЛЖИТЕЛЬНОСТЬ:
${duration}


1. ВАЖНОЕ ПРИМЕЧАНИЕ ДЛЯ УЧИТЕЛЯ

Этот урок создан на основе материала, который учитель вставил в приложение. Если информации недостаточно, перед проведением урока нужно проверить факты и при необходимости добавить дополнительные источники.


2. ИСТОЧНИКИ И ССЫЛКИ, УКАЗАННЫЕ УЧИТЕЛЕМ

${linksBlock}


3. ИСХОДНЫЙ МАТЕРИАЛ УЧИТЕЛЯ

${sourceMaterial}


4. ЦЕЛЬ УРОКА

Сформировать у учащихся представление по теме «${topic}», объяснить основные факты и показать значение темы для понимания истории Алтая и Алтайского края.


5. ЗАДАЧИ УРОКА

Образовательные:
- познакомить учащихся с основными сведениями по теме;
- объяснить ключевые факты и понятия;
- показать связь темы с историей региона.

Развивающие:
- развивать умение работать с исторической информацией;
- учить выделять главное;
- формировать умение делать выводы.

Воспитательные:
- воспитывать интерес к истории родного края;
- формировать уважение к исторической памяти;
- показать ценность изучения региональной истории.


6. ГОТОВЫЙ ТЕКСТ ДЛЯ ОБЪЯСНЕНИЯ УЧИТЕЛЯ

Сегодня мы изучаем тему «${topic}». Эта тема помогает нам лучше понять историю Алтая и Алтайского края, увидеть связь прошлого родного региона с историей России и жизнью людей.

Учителю можно начать объяснение с вопроса к классу: что ученики уже знают по этой теме и почему она может быть важна для жителей Алтайского края.

Далее учитель опирается на следующий материал:

${sourceMaterial}

После чтения и объяснения материала важно обратить внимание учащихся на главные факты, имена, даты, события и понятия. Если в тексте встречаются сложные слова, их нужно объяснить простым языком и записать на доске.

Главная задача урока — не просто пересказать информацию, а помочь ученикам понять, почему эта тема важна. Для этого можно задавать вопросы: где происходили события, кто участвовал, какие были причины, какие последствия, как это связано с историей Алтая и России.

В конце объяснения учитель подводит учеников к выводу: изучение истории родного края помогает увидеть, что большая история страны складывается из историй отдельных регионов, городов, сёл, семей и людей.


7. ВОПРОСЫ ДЛЯ БЕСЕДЫ

1. О чём была сегодняшняя тема?
2. Какие факты показались вам самыми важными?
3. Какие новые понятия вы узнали?
4. Почему эта тема важна для истории Алтая?
5. Как изучаемая тема связана с историей России?
6. Какой вывод можно сделать?


8. ЗАДАНИЯ ДЛЯ ЗАКРЕПЛЕНИЯ

Задание 1. Выпишите главное.
Запишите 3–5 главных фактов по теме «${topic}».

Задание 2. Объясните своими словами.
Объясните, почему эта тема важна для понимания истории родного края.

Задание 3. Составьте вопросы.
Придумайте 3 вопроса по теме урока.

Задание 4. Сделайте вывод.
Продолжите фразу:
«Тема “${topic}” важна, потому что...»


9. МИНИ-ПРОВЕРКА

1. Назовите тему урока.
2. Какие факты вы запомнили?
3. Какие понятия были главными?
4. Что было самым сложным?
5. Какой главный вывод можно сделать?


10. ДОМАШНЕЕ ЗАДАНИЕ

Вариант 1.
Подготовить краткий рассказ по теме «${topic}» на 7–10 предложений.

Вариант 2.
Составить 5 вопросов по теме.

Вариант 3.
Найти дополнительный факт, иллюстрацию или короткую справку по теме.

Вариант 4.
Подготовить мини-сообщение на 1–2 минуты.


11. ПЛАН ЗАПИСИ НА ДОСКЕ

Тема: ${topic}

Главные вопросы:
1. О чём эта тема?
2. Какие факты самые важные?
3. Почему это важно для истории Алтая?
4. Какой вывод можно сделать?

Главный вывод:
История родного края помогает лучше понять прошлое своего региона и связь местной истории с историей России.
`.trim();
}
